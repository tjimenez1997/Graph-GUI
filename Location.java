public class Location {
    /**
     * Container Object created to easily send X and Y Coordinates as one parameter
     */
    private double x,y;

    public double getX(){
        return x;
    }

    public double getY(){
        return y;
    }

    public void setX(double value){
        x = value;
    }

    public void setY(double value) {
        y = value;
    }
}
